    private void aceptarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarjButtonActionPerformed
        LocalDate fechaInicioRegistroFactura = LocalDate.of(jYearChooser.getValue(), jMonthChooser.getMonth() + 1, 1);
        DataBase.insertRegistroFactura(mainFrame.getArrendatario().getDni(), mainFrame.getInmueble().getIdInmueble(), 1, fechaInicioRegistroFactura, false, false, false, mainFrame.getContrato().getIdContrato());
        mainFrame.rellenarTablaRegistroFactura(DataBase.selectRegistroFacturaByDniArrendatarioAndIdInmueble(mainFrame.getArrendatario().getDni(), mainFrame.getInmueble().getIdInmueble()));
        mainFrame.getFechaInicioTextField().setText(fechaInicioRegistroFactura.plusMonths(1).toString());
        mainFrame.getFechaFinTextField().setText(fechaInicioRegistroFactura.plusMonths(mainFrame.getNumeroMesesSpinner()).toString());
        mainFrame.setUltimoRegistroFactura(DataBase.selectRegistroFacturasUltimaFechaFacturaFacturaByDniArrendatarioAndIdInmueble(mainFrame.getArrendatario().getDni(), mainFrame.getInmueble().getIdInmueble()));
        dispose();
        mainFrame.setVisible(true);
    }//GEN-LAST:event_aceptarjButtonActionPerformed


